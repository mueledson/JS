//                                              For
//  O for ele tem a sintaxe mais complexa, mas é mais usada
//  mesmo que parece ser mais difícil, como a instrução fica toda em uma linha proporciona maior controle

// A e é importante dizer que todos os passos são separados por ;
// No for Primeiro temos que iniciar uma variável, Segundo temos que fazer a candição, e Terceiro nós podemos também fazer a variável++
let arr = [1, 2, 3, 4] 

for (let j = 0; j < arr.length; j++) {
    console.log("Repetindo for: " + arr[j])
}

console.log(arr.length)

for (var x = 5; x < 100; x *= 3) {
    console.log(x)
}

